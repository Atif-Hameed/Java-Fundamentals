public class ArraysLearns {
    public static void main(String[] args){
        String[] myArray = {"A","T","I","F"};
        System.out.print("[ ");
        for (int i =0; i<4; i++){
            System.out.print(myArray[i]+" ");
        }
        System.out.print("]");

        int[] myArray2 = {9,8,7,6};
        System.out.print("[ ");
        for (int i =0; i<4; i++){
            System.out.print(myArray2[i]+" ");
        }
        System.out.print("]");
    }
}
